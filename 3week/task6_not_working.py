from typing import List


class Solution:
    def threeSum(self, nums: List[int]) -> List[List[int]]:
        results = list()
        nums.sort()
        left_ptr, right_ptr = 0, 0
        for i in range(len(nums)):
            # print(f"i is {i}/{len(nums) - 1}")
            first = nums[i]
            left_ptr, right_ptr = 0, len(nums) - 1
            while left_ptr < right_ptr:
                # print(f"i={i}, l={left_ptr}, r={right_ptr}")
                if i == left_ptr or (left_ptr > i and nums[left_ptr] == nums[left_ptr-1]):
                    left_ptr += 1
                    continue
                if i == right_ptr or (right_ptr < i and nums[right_ptr] == nums[right_ptr + 1]):
                    right_ptr -= 1
                    continue
                if left_ptr == right_ptr:
                    break
                if i != len(nums)-1 and nums[i] == nums[i+1]:
                    break
                # print(f"left is {left_ptr}, right is {right_ptr}, i is {i}")
                if nums[left_ptr] + nums[right_ptr] == -first:
                    # print(f"APPENDING: i={i}, l={left_ptr}, r={right_ptr}")
                    results.append([first, nums[left_ptr], nums[right_ptr]])
                        # print(f"Append with {first}, {nums[left_ptr]}, {nums[right_ptr]}")
                    left_ptr += 1
                elif nums[left_ptr] + nums[right_ptr] < -first:
                    left_ptr += 1
                else:
                    right_ptr -= 1
        return results


